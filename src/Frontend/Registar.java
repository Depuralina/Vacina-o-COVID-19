    private void guardar() {
        if (jSpinner1.getValue().equals(0)) {
            JOptionPane.showMessageDialog(this, "Introduza número do SNS");
            jSpinner1.requestFocus();
            return;
        }
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a Morada");
            jTextField2.requestFocus();
            return;
        }
        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a Localidade");
            jTextField3.requestFocus();
            return;
        }
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a data de nascimento");
            jTextField4.requestFocus();
            return;
        }
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o contacto");
            jTextField5.requestFocus();
            return;
        }
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o email");
            jTextField6.requestFocus();
            return;
        }
        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o nome");
            jTextField7.requestFocus();
            return;
        } 
        try {
            sistema.getListaUtentes().set_Utente(new Utente((Integer) jSpinner1.getValue(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),jTextField5.getText(),jTextField6.getText(),jTextField8.getText(),jTextField9.getText()));
        
        } catch (ListaUtentes.UtenteDuplicadoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }
        try {
            sistema.getListaUtilizadores().set_Utilizador(new Utente((Integer) jSpinner1.getValue(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),jTextField5.getText(),jTextField6.getText(),jTextField8.getText(),jTextField9.getText()));
        } catch (ListaUtilizadores.UtilizadorDuplicadoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }
        for(CentroVac m: sistema.getListaCentros().get_Centros())
        {
            if(jTextField3.getText().equals(m.get_Localidade()))
            {
                try {
                    sistema.getListaUtentes().get_Utente(jTextField8.getText()).set_Centro(m);
                } catch (ListaUtentes.UtenteNaoExistenteException ex) {
                    Logger.getLogger(Registar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
       JOptionPane.showMessageDialog(this, "Registo guardado com sucesso.");
        dispose();

    }
